function pdemodel
[pde_fig,ax]=pdeinit;
pdetool('appl_cb',1);
set(ax,'DataAspectRatio',[1 1 1]);
set(ax,'PlotBoxAspectRatio',[655.20000000000005 436.90666666666675 291.27111111111117]);
set(ax,'XLimMode','auto');
set(ax,'YLim',[-2.5 2.5]);
set(ax,'XTickMode','auto');
set(ax,'YTickMode','auto');
pdetool('gridon','on');

% Geometry description:
pdeellip(0,0,0.5,0.5,...
0,'E1');
pdepoly([ 0,...
 -1,...
 -1,...
 0,...
 1,...
 1,...
],...
[ 2,...
 1,...
 -1,...
 -2,...
 -1,...
 1,...
],...
 'P1');
set(findobj(get(pde_fig,'Children'),'Tag','PDEEval'),'String','P1-E1')

% Boundary conditions:
pdetool('changemode',0)
pdesetbd(10,...
'dir',...
1,...
'1',...
'1/4')
pdesetbd(9,...
'dir',...
1,...
'1',...
'1/4')
pdesetbd(8,...
'dir',...
1,...
'1',...
'1/4')
pdesetbd(7,...
'dir',...
1,...
'1',...
'1/4')
pdesetbd(6,...
'dir',...
1,...
'1',...
'2*(x.^2+2.*x+2)')
pdesetbd(5,...
'dir',...
1,...
'1',...
'2*(x.^2-2.*x+2)')
pdesetbd(4,...
'neu',...
1,...
'0',...
'2*(2+x.*y)')
pdesetbd(3,...
'dir',...
1,...
'1',...
'2*(x.^2-2.*x+2)')
pdesetbd(2,...
'dir',...
1,...
'1',...
'2*(x.^2+2.*x+2)')
pdesetbd(1,...
'neu',...
1,...
'0',...
'2*(2+x.*y)')

% Mesh generation:
setuprop(pde_fig,'trisize',0.10000000000000001);
setuprop(pde_fig,'Hgrad',1.3);
setuprop(pde_fig,'refinemethod','regular');
pdetool('initmesh')

% PDE coefficients:
pdeseteq(1,...
'(2+x.*y)',...
'0.0',...
'(-8*(1+x.*y))',...
'1.0',...
'0:10',...
'0.0',...
'0.0',...
'[0 100]')
setuprop(pde_fig,'currparam',...
['(2+x.*y)     ';...
'0.0          ';...
'(-8*(1+x.*y))';...
'1.0          '])

% Solve parameters:
setuprop(pde_fig,'solveparam',...
str2mat('0','2760','10','pdeadworst',...
'0.5','longest','0','1E-4','','fixed','Inf'))

% Plotflags and user data strings:
setuprop(pde_fig,'plotflags',[1 1 1 1 1 1 7 1 0 1 0 1 1 0 1 0 0 1]);
setuprop(pde_fig,'colstring','');
setuprop(pde_fig,'arrowstring','');
setuprop(pde_fig,'deformstring','');
setuprop(pde_fig,'heightstring','');

% Solve PDE:
pdetool('solve')
